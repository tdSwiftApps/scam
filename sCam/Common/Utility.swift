import Foundation
import UIKit

class Utility {
    
    /**
     * parameter CGFloat
     * return CGFloat
     * 角丸の値を返す
     */
    static func roundedCornerValue(width: CGFloat) -> CGFloat {
        return width * 0.05
    }
    
}
